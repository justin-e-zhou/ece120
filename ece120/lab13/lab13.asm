;This program prints the ASCII character at memory location x5002 and prints
;it using a large font, formed out of characters specified at locations x5000
;and x5001.
;
;To find the font of the character, a counter starting from the character
;decrements until it reaches zero. Every time it decrements, 16 is added to
;another counter that starts from the beginning of the font table. When the
;first counter decrements to zero, the font for the letter is reached.
;
;Once it reaches the font, two more counters are initialized, one for the row
;and one for the column. If the leftmost bit of the font is a 0, the character
;from location x5000 is printed. If the leftmost bit is a 1, the character from
;location x5001 is printed. This continues until the column counter reaches
;zero, and then it prints a newline character. This process repeats until the
;row counter reaches zero.
;
;Register Table:
;R0 = location for the character and storage for printing 0 and 1 characters
;R1 = location for the one character
;R2 = location for the zero character
;R3 = location of the font
;R4 = row counter
;R5 = column counter
;R6 = contains the character counter and the font
	.ORIG	x3000
	
	LEA	R1, FONT_DATA	; initialize R1 with FONT_DATA
	LD  R2, WORD      ; initialize R2 with WORD
	
					; row counter
	AND	R4, R4, #0
	ADD	R4, R4, #15
	ADD	R4, R4, #1
  BRnzp START

NXT_LTR  ADD R2, R2, #1  ; increments R2

				            ; finding font for the char
START	AND R3, R3, #0    ; clear R3
	ADD R3, R3, R1    ; load R3 with R1
	LDR R6, R2, #0    ; load R6 with character
	BRz NXT_LN        ; if letter = NULL, start new line
FND_FNT	ADD	R3, R3, #15	; add 16 to R3
	ADD	R3, R3, #1
	ADD	R6, R6, #-1	; decrement R6
	BRp	FND_FNT		; loop until R6=0

				; column counter
PRNT_ST	AND	R5, R5, #0
	ADD	R5, R5, #8

				; printing
	LDR	R6, R3, #0	; load start of font into R6
PRNT_LP	BRzp	SKIP		; R6=negative (leftmost bit is 1)?
  LD  R3, OCHAR ; loads location of one character into R3
	LDR R0, R3, #0	; load one character into R0
	OUT			; prints one character
	BRnzp	NEXT		; branches to NEXT
SKIP	 LD  R3, ZCHAR ; loads location of one character into R3
	LDR R0, R3, #0	; load zero character into R0
	OUT			; prints zero character
NEXT	ADD	R5, R5, #-1	; decrements column counter
	BRz	NXT_LTR		; column counter = 0?
	ADD	R6, R6, R6	; left shifts R6
	BRnzp	PRNT_LP		; loops

NXT_LN	LD	R0, NL_CHAR	; load NewLine ASCII char
	OUT
	ADD R1, R1, #1  ; increments where FONT_DATA starts
	LD  R2, WORD    ; resets R2 to start at first letter again
	ADD	R4, R4, #-1	; decrement row
	BRz	DONE		; halts if end
	BRp	START		; loops back to print new line

NL_CHAR	.FILL xA; new line char

WORD	.FILL	x5002
ZCHAR .FILL x5000
OCHAR .FILL x5001

DONE	HALT

